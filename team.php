            <h2>Key Features</h2>
            <ul>
                <li>Full-stack Website: The website is constructed following the Model-View-Controller (MVC) architecture, encompassing both front-end and back-end development, including database management.</li>
                <li>Admin Panel: The back end of this website incorporates an administrative panel for efficient management.</li>
                <li>Access for Honorable Advisors: It grants exclusive access to Honorable Advisors of BAIUST Computer Club and Club Executive Panel members.</li>
            </ul>
            </br>
            <h2>Back-End Modules</h2>
            <ul>
                <li>Members' Database: A comprehensive database of club members is maintained.</li>
                <li>Voter List Generation: The system automates the generation of voter lists from the membership database.</li>
                <li>Certificate Module: This module oversees the management of certificates and their associated database.</li>
                <li>Front-End Editing: Front-end components, including workshops and the Executive Panel, can be edited seamlessly.</li>
            </ul>
            </br>
            <h2>BAIUST Programmers' Hub</h2></br>
            <h3>Introduction</h3>
            <p>This Module aims to provide an inclusive platform for programmers' growth and engagement.</p>
            <p>Seeks to overcome challenges in organizing and streamlining programmer data and rankings.</p>
            </br>
            <h3>Modules</h3>
            <ul>
                <li><strong>User Profile Management:</strong> Efficiently manage programmer profiles. Features for registration, login, and updates. Facilitates assignment of performance marks.</li>
                <li><strong>Admin Dashboard for Scoring:</strong> Allows faculty to assign performance scores and rankings. Ensures fair and accurate programmer evaluation.</li>
                <li><strong>BAIUST Programmers' Rank List Page:</strong> Dedicated webpage for programmers to view rankings and achievements. Promotes transparency, showcasing exceptional accomplishments.</li>
                <li><strong>Coder of the Month Page:</strong> Every Month's #no.1 Programmer is stored in CoM Table of Database. Shown in table format Coder of the Month Page.</li>
